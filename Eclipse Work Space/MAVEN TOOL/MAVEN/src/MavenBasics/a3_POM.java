/*
POM
=========

POM stands for project object model.

POM is the fundamental unit of maven.

POM is an XML file that contains information about the project and configuration details
used by Maven to  build the project.

POM contains default values for projects like build directory, which is target; the source
directory, which  is src/main/java; the test source directory, which is src/test/java; and so
on.

When we execute MAVEN project then MAVEN will look for the project configurations in
pom.xml file and gets  the needed things and executes the project.


In Building MAVEN Projects, pom.xml file contains the following configurations.

1.  Project Description
2.  Repository
3.  Dependency Management
4.  Project Inheritance
5.  Build Configuration
6.  Build Profiles

*/