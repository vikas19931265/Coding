package MavenBasics;
/*
What is Maven
===================

MAVEN is a "Project Management Framework", it is much more than a simple Build tool.

MAVEN does the following activities of the project lifecycle automatically

1. Provides default project structure.
2. Download required dependencies ( jar files)
3. Compiles source code.
4. Packaging project ( jar , war and ear)
5. Starts the server
6. Deploying projects in server.
7. Performs unit testing.
8. Prepares test reports
9. Preparing documentation.
10.Undeploy applications from the server.
11.Stops the server.

*/

