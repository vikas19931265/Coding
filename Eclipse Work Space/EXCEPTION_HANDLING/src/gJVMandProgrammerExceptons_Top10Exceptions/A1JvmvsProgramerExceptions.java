/*

Types of Exceptions
=============================

1. Based on person who is raising an exception all exceptions are divided into two categories.

  i) JVM Exceptions.
    
  ii) Programmatic Exceptions.


JVM Exceptions.
==============================

1. The Exceptions which are raised automatically by th JVM whenever a particular event occurs
   are called as JVM exceptions example ArithmeticException, NullPointerException etc.


Programmatic Exceptions.
==============================

1. The Exceptions which are raised explicitly either by the programmer or by the API developer to 
   indicate that something goes wrong are called as programatic exceptions.
    

Example : TooOldException

          IllegalArgumentException
 
          etc.




*/

