package springcore;
/*
Constructor with index
========================

While building constructor argument in xml its mandatory to maintain the order which is a teadious
task and hence therefore we can use index position by which we can keep the arguments in any order
and update the index position accordingly


It is recommended to use index or type attributes while working with constructor injection.

Inserting null
=================

<constructor-arg index="7">
				<null></null>
		</constructor-arg>
		
		
Inserting empty string
=========================		
		
<constructor-arg index="6">
			<value>""</value>
		</constructor-arg>

 */ 
