/* 
Null
=============

The null value indicates an empty value; it is essentially a placeholder that represents “nothing”. 
The null value is defined as an empty object so using typeof operator on a variable holding null 
shows its type to be object.

Undefined Vs Null
=====================

Undefined means the value hasn’t been set, whereas null means the value has been 
set to be empty.

*/

var a = null;
console.log(a );  // null 
console.log(typeof(a) ); // object
