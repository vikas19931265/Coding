/* 
    Infinity
    ============

Infinity or -Infinity is the value JavaScript will return if a number is too large or 
too small. All Infinity values compare equal to each other. 

Ex:- 

document.write(5 / 0); 	// infinity
document.write(- 5  / 0); 	// - infinity 


*/

console.log(5/0); // Infinity
console.log(-5/0); //-Infinity