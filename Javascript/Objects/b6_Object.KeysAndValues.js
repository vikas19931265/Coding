/* 
    Object.keys(objectname);
    ============================

        This function is going to return the keys. The return type is array.

    Object.values(objectname);
    
        This function is going to return the values and return type is array.

*/
let students=
{
    name: "vikas",
    id:1234
}


console.log(Object.keys(students)); // name, id
console.log(Object.values(students)); // vikas, 1234