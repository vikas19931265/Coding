/* 

Arrays
====================

Arrays are collection of data items stored under a single name. 

Array provide a mechanism for declaring and accessing several data items with only 

one identifier, thereby simplifying the task of data management.

We use array when we have to deal with multiple data items.
Arrays are a special type of objects. 

The typeof operator in JavaScript returns "object" for arrays.

*/

var elements= []; // empty array
console.log(typeof(elements));  // object.  Array is of object type