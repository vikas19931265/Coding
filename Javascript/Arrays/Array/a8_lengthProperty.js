/* 

Length Property
=========================

The length property retrieves the index of the next available position at the end 
of the array. 

The length property is automatically updated as new elements are 
added to the array. 

For this reason, length is commonly used to iterate through 
all elements of an array. 

var geek = [“Rahul”, “Ram”, 56, “Jay”]; 
document.write(geek.length); 

*/

var elements=[0,1,2,3];
console.log(elements.length); //4