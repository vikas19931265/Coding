/*

Syntax to provide the initialization parameter in Context scope
==========================================================================

The context-param element, subelement of web-app, is used to define the initialization parameter
in the application scope. The param-name and param-value are the sub-elements of the context-param. 
The param-name element defines parameter name and and param-value defines its value.

<web-app>  
 ......  
      
  <context-param>  
    <param-name>parametername</param-name>  
    <param-value>parametervalue</param-value>  
  </context-param>  
 ......  
</web-app>  


*/