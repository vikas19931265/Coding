/*

Introduction
===============================================================

Multitasking

    Executing several tasks simultaneously is the concept of the multitasking. There
are two types of multitasking.

1. Process Based Multitasking
2. Thread  Based Multitasking


Process Based Multitasking
===========================

Executing several tasks simultaneously where each task is separate independent process
such type of multitasking is called as process based multitasking.

Example

While typing java program in the editor we can listen to mp3 audio songs at the same
time we can download a file from the net. All these tasks are independent from one
another executing simultaneously and hence it is process based multitasking.

This type of multitasking is best suitable at the "OS level"


Thread Based Multitasking
==========================

Executing several tasks simultaneously where each task is SEPARATE INDEPENDENT PART
OF THE SAME PROGRAM IS CALLED AS THREAD BASED MULTITASKING.

1. This type of multitasking is best suitable for the programmatic level.

2. When compared with "C++" developing multithreading applications is very easy in java
because java provides in built support for multithreading through rich API(Thread,
Runnable, ThreadGroup, ThreadLocal etc).

3. In multithreading  10% of the work the programmer is required to do and 90%
of the work and is done by the java API.

4. The main important application areas of  multithreading are.

    a. To implement multimedia graphics
    b. To develop animations
    c. To develop video games
    d. To develop web and application servers

5. Whether it is process based or Thread based the MAIN OBJECTIVE OF THE MULTITASKING
IS TO IMPROVE THE PERFORMANCE OF THE SYSTEM BY REDUCING THE RESPONSE TIME.


*/