/*

Java multi threading concept is implemented by using the following two models

1. Green thread model

2. Native OS model.


Green Thread model.


The thread which is managed completely by the JVM without taking underling  OS support is called 
as Green Thread. Very few operating systems like SUN Solaris provides support for green thread model.

Any way green Thread model is deprecated now and not recommended to be used.


Native OS Model.

The thread which is managed by the JVM with the help underlying  operating system , is called as Native OS model.
All windows based operating systems  provides support for native OS model.






*/