/*

How to stop a Thread.
==================================

We can stop a thread execution by using stop() method of the thread class.

public void stop()

    If we call stop() method then immediately the thread will enter into the dead state.
Any way stop() method is deprecated and not recommanded to be used.



How to supspend and resume of a Thread.
====================================================

   
We can suspend a thread by using suspend() method of thread class then immediately the thread will be entered into suspended state.

    public void suspend();

We can resume a suspeded thread by using resume() method of the thread class , then suspended thread can continue its execution.   

    public void resume();

Any way these methods are deprecated and not recommanded to be used




*/
