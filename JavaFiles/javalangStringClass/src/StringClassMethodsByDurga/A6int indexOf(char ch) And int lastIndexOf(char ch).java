/*

public int indexOf(char ch);

        returns index of first occurrence of the specified character.


public int lastIndexOf(char ch);

      returns index of last occurrence of the specified character.

*/
package StringClassMethodsByDurga;


 class index {

     public static void main(String[] args)
     {
         String s ="ababa";

         System.out.println(s.indexOf('a'));  //0

         System.out.println(s.lastIndexOf('a'));//4
     }



 }

