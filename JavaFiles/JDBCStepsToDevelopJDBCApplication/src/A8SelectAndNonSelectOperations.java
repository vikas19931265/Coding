/*

Select Operations and Non-Select Operations
================================================================================

Select Operations:
=====================

Whenever we are performing Select Operation then we will get a Group of Records as Result.
Eg: select * from movies;


Non-Select Operations:
=======================

Whenever we are performing Non-Select Operation then we will get Numeric Value that
represents the Number of Rows affected.

Eg: update movies set heroine='Tamannah' where no=1;

Once we create Statement Object, we can call the following Methods on that Object to execute
our Queries.

1. executeQuery()
2. executeUpdate()
3. execute()


*/