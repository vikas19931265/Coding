/* Linked List
================================================================================


1. A doubly linked list whose nodes contain three fields: an integer value, 
   the link to the next node, and the link to the previous node.

2. java linked list also follows the same structure.


-----------        --------------       ----------------       ----------------     
| * | a |  |<------|   | b   |    |---->|      | c  |   |----> |    |  d  |     |    
|----------        |-------------<----- |--------------- <---- |----------------  

                    

3. The underline data structure for Linked List is doubly linked list.

4. Insertion order is preserved.

5. Duplicate objects are allowed.

6. Heterogeneous objects are also allowed.

7  Null insertion is allowed.

8. Linked list implements Serilizable and Cloneable interfaces but not 
   RandomAccess.

9. Linked list is the best choice if our frequent operation is insertion 
   or deletion in the middle.

10. Linked list is the worst choice if our frequent operation is retrieval
    operation.


Some more Points.
=================================

The important points about Java LinkedList are:

    Java LinkedList class can contain duplicate elements.
    
    Java LinkedList class maintains insertion order.
    
    Java LinkedList class is non synchronized.
    
    In Java LinkedList class, manipulation is fast because no shifting needs to be occurred.
    
    Java LinkedList class can be used as list, stack or queue*/


