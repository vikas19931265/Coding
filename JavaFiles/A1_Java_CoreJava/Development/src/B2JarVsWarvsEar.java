
/*

Jar  vs War  vs Ear
===============================================================================

JAR(Java Archive File)
======================

It contains a group of .class Files.


WAR(Web Archive )
=====================

A WAR file represents one Web application which contains Servlets , JSPS, HTML
pages , JAVA Script files etc.

The main advantage of maintaining a web application in the form of the WAR file is
project deployment, project delivery and project transportation will become easy.


EAR(Enterprise Archive)
=========================

An EAR file represents one enterprise application which can contains Servlets, JSPS
EJBs, JMS components etc.


note
========

In general EAR file represents a group of war files  and the jar files


*/