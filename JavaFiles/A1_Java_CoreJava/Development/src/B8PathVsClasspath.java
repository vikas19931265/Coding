

/*

Difference between the path and the classpath??
===============================================================================

classpath
===================

Classpath describes the location where required .class files are available.

Java compiler and JVM will use the classpath to locate the required class files.
If we are not setting the classpath then our program may not compile and may not
run.


Path
=========================

Path describes the location where required binary executable are available. If we
are not setting the path then javac and java command will not work


example

        set path =C:\program Files\java\jdk 1.6\bin


*/