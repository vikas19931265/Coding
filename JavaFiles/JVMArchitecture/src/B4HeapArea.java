
/*

Heap Area
=============================

For every JVM one heap area will be available. Heap area will be created at the time of
JVM startup.

Objects and the corresponding instance variables will be stored inside the heap area only.

Every array in java is also object only and hence Arrays also will be stored in the heap 
area only.

Heap area can be accessed by multiple threads and hence the data stored in the heap memory
is not the thread safe.

Heap area need not be continuous memory.


*/