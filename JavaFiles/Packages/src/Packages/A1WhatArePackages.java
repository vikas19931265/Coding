/* What are packages?
===============================================================

1. It is an encapsulation mechanism to group related classes and interfaces into a single unit.
   Which is nothing but a package.

Example1

    All classes and interfaces which are required for database operations are grouped into a single package
    which is nothing java.sql package.

Example 2:

    All classes and interfaces which are useful for file I/O operations are grouped into a separate package
    which is nothing but java.io package.


2. The main advantages of package are

    1) To resolve naming conflicts.( That is unique identification of our components)
    
    2) It improves modularity of the application.

    3)It improves maintainability of the application.

    4)It provides security for our components( example a default class cannot be accessed outside the package).


*/

