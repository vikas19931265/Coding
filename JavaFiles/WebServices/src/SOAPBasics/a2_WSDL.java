/*
WSDL Usages
============

WSDL is often used in combination with SOAP and XML Schema to provide web services over the Internet. 
A client program connecting to a web service can read the WSDL to determine what functions are available on 
the server. 

Any special datatypes used are embedded in the WSDL file in the form of XML Schema. The client can then 
use SOAP to actually call one of the functions listed in the WSDL


*/