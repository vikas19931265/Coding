package Operators;

/*

Compound Assignment operator
--------------------------------------------

Sometimes assignment operator is mixed with some other operator. Such type of assignment
operators are called as Compound Assignment operators.


The following are all possible compound assignment operators in java

+=;     &=          >>=(right shift)

-=;     |=          >>>=(unsigned right shift)

*=;     ^=          <<=(left shift)

/=;




*/
 class CompoundAssignment {
    

     public static void main(String[] args)
     {
         int a =10;
         
         a+=20;
         
         System.out.println(a);//30
                 
                 
     }
 
 
 }

