package Operators;

/*

new operator
===========================

We can use new operator to create Objects 

    Example

                Test t = new Test();

note
---------

1. After creating an object constructor will be executed to perform initialization 
   of an object hence Constructor is not for creation of the objects but it is 
   for initialization of an object.


2. In java we have only new keyword but not delete keyword because destruction of 
   useless objects is the responsibility of garbage collector.




[] operator
------------------

We can use [] operator to declare and create arrays example.

            int[] x = new int[7];



*/
