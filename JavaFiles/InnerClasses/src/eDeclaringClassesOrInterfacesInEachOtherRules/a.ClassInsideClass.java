package eDeclaringClassesOrInterfacesInEachOtherRules;

/*

Various combinations of nested classes and interfaces.
---------------------------------------------------------

Case 1:

      class inside a class.


Without existing one type of object, if there is no chance of existing another type of object then we can declare
a class inside another class.
	
Example

       University consists of several departments. Without existing university there is no chance of existing 
       department hence we have to declare department class inside the university class.


       class University
        {

            class Department
            {
            
            }
        }    




*/

