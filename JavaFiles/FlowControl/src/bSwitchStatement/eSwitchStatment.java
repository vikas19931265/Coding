/*

switch(x) // statement
------------------------

1. If several options are available then it is not recommended to use nested if else statements
   because if reduces readability of the code.

2. To handle this requirement we should go for switch() statement.

3. The case inside the switch statement has to be of x type in switch(x).


syntax of switch() statement
--------------------------------


switch(x)

{

case 1: Action-1
        break;

case 2: Action-2
        break;

.
..
.

case n: Action-n 
        break

default: default Action


}


How will you read switch statement??
-----------------------------------

switch(x)

        here x acts like as a variable.

case 1(1 is label)

      and label acts like values of the variable


case 2


So we can read it like, when the value of x is 1 do this,

or else when the value of x is 2 then do this

*/

