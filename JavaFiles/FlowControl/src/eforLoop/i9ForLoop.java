/*

for loop
------------------


1. for loop is the most commonly used loop in java.

2. If we know the exact number of iterations in advance then for-loop is the 
   best choice.


Syntax of for-loop
--------------------


for(initialization-section ; conditional-check ; increment or decrement section)
    {	
        // loop body

}


1.**** First initialization-section is executed. This section is executed only once in the entire
   for loop journey.

2. Next conditional-Check statement will be executed.

3. If the condition is true then the body of for loop is executed.

4. After this increment or decrement operation will be executed.

5. And then again conditional check will be executed.

6. And then again body and thereafter increment or decrement section.

7. This loop continues until the test condition is true.

*/


