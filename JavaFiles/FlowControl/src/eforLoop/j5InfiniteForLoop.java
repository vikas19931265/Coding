/*

Infinite for loop
----------------------------


All three parts of for loop are independent of each other and optional also

*/


class InfiniteLoops
{
    
    public void infiniteloop1()
    {
        
        for(; ; )
        {
            System.out.println("hello");
        }
    }



    public void infiniteloop2()
    {
        for( ; ; ); // infinite loop
    }
    


}

