/*

Wrapper Classes
===============================================================================

The main objective of wrapper class is 

        1. To wrap primitive into the objective form so that we can handle
           primitives just like objects.


        2. To define several utility methods which are required for the primitives.



Wrapper class in java provides the mechanism to convert primitive into object and object into primitive.

Since J2SE 5.0, autoboxing and unboxing feature converts primitive into object and object into primitive 
automatically. The automatic conversion of primitive into object is known as autoboxing and vice-versa unboxing.

The eight classes of java.lang package are known as wrapper classes in java. The list of eight wrapper 
classes are given below:


Primitive Type      Wrapper class

boolean             Boolean
char                Character
byte                Byte
short               Short
int                 Integer
long                Long
float               Float
double              Double




*/

