
/*

What is the difference between the web server and the application server???
================================================================================

Web server provides the environment to run web application. Web server provides 
the support for web related technologies like servlets, JSPs, HTML etc. Example 
Tomcat.


Application server provides environment in order to run enterprise applications. Application
server can provide support for any technology from J2EE like servlets, JSPs , EJB, JMS
components etc, example Weblogic, Websphere, JBoss etc.


Every application server contains in built web server to provide support for web
related technologies.


J2EE compatible server is the application server.

*/