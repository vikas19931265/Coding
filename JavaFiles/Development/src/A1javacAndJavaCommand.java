 /*

Development
=============================

javac command
=============================

    We can use javac command to compile a single or a group java source files.
    
    
javac [options] Test.java
javac [options] A.java B.java C.java
javac [options] *.java

options

-version
-d 
-source
-verbose
-cp or -classpath



java command
============================

We can use the java command to run a single class file.

syntax
    java [options]  Test A B C {A,B,C are the command line arguments}.

options
    -version
    -D
    -cp or -classpath
    -ea|-esa|-dsa|-da
    

note

    We can compile any number of source files at a time but we can run only one .class
file at a time. */

