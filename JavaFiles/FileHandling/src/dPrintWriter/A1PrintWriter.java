package dPrintWriter;

/*

PRINTWRITER
=============



1. PrintWriter is the most enhanced writer to write  character data to the file.

2. The main advantage of PrintWriter over file FileWriter and BufferedWriter is that
   we can write any type of the primitive data directly to the file


Constructors in PrintWriter
==================================

1. PrintWriter pw = new PrintWriter(String fname);

2. PrintWriter pw = new PrintWriter(File f );

3. PrintWriter pw = new PrintWriter(Writer w);



note
======================================

PrintWriter can communicate directly with the File and can communicate via some writer
object also.


*/